<script>
    export let chapterNo;

    import Next from './Next.svelte';
    import audio1 from '../../static/audio/1.mp3';
    import audio2 from '../../static/audio/2.mp3';
    import audio3 from '../../static/audio/3.mp3';

    let takeawayEl;
    let headerEls;
    let instructionEls;
    let horizontalLineEls;
    let verticalLineEls;
    let nextButtonEl;
    let audioButtonEls;
    let audioTextEls;
    let audioPlayers;

    let active = false;
    let iteration = 0;

    export function animate() {
        reset();
        const stagger = 2000;
        const wait = 2000;
        // this is to only animate the current iteration,
        // in case the user switches back and forth fast
        let currIteration = iteration;
        active = true;

        headerEls.forEach((el, i) => {
            setTimeout(() => {
                 active && currIteration === iteration && el.classList.add('active');
            }, (i * stagger) + wait / 2);
        })
        
        instructionEls.forEach((el, i) => {
            setTimeout(() => {
                 active && currIteration === iteration && el.classList.add('active');
            }, (i * stagger) + wait);
        })

        audioTextEls.forEach((el, i) => {
            setTimeout(() => {
                 active && currIteration === iteration && el.classList.add('active');
            }, wait + stagger * instructionEls.length - 1000);
        })

        horizontalLineEls.forEach((el, i) => {
            setTimeout(() => {
                 active && currIteration === iteration && el.classList.add('active');
            }, (i * stagger / 2) + 300);
        })

        verticalLineEls.forEach((el, i) => {
            setTimeout(() => {
                 active && currIteration === iteration && el.classList.add('active');
            }, (i * stagger / 2.5));
        })

        setTimeout(() => {
            nextButtonEl && nextButtonEl.classList.add('active');
        }, instructionEls.length * stagger + stagger);
    }

    export function reset() {
        active = false;
        iteration++;

        nextButtonEl && nextButtonEl.classList.remove('active');

        headerEls.forEach((el, i) => {
            el.classList.remove('active');
        })

        audioTextEls.forEach((el, i) => {
            el.classList.remove('active');
        })

        instructionEls.forEach((el, i) => {
            el.classList.remove('active');
        })

        horizontalLineEls.forEach((el, i) => {
            el.classList.remove('active');
        })

        verticalLineEls.forEach((el, i) => {
            el.classList.remove('active');
        })

        audioPlayers.forEach((el, i) => {
            el.pause();
            el.currentTime = 0;
        })

        setAudioElActiveStates();
    }

    const setAudioElActiveStates = () => {
        audioPlayers.forEach((player, j) => {
            if (!player.paused) {
                audioTextEls[j].classList.add('playing');
            } else {
                audioTextEls[j].classList.remove('playing');
            }
        })
    }

    const setAudioEvents = () => {
        audioButtonEls.forEach((el, i) => {
            el.addEventListener('click', () => {

                audioPlayers.forEach((player, j) => {
                    if (j !== i) {
                        player.pause();
                    }
                })

                let playing = !audioPlayers[i].paused;
                if (!playing) {
                    audioPlayers[i].play();
                } else {
                    audioPlayers[i].pause();
                }

                setAudioElActiveStates();

            });
        })
    }

    const checkInactivity = () => {
        audioPlayers.forEach((el, i) => {
            el.addEventListener("timeupdate", function () {
                window.state.resetInactivity();
            }); 
        });
    }
    
    document.addEventListener('DOMContentLoaded', function(event) {
        takeawayEl = document.querySelector(`.takeaway-audio-${chapterNo}`);
        headerEls = takeawayEl.querySelectorAll('.header');
        instructionEls = takeawayEl.querySelectorAll('.instruction');
        horizontalLineEls = takeawayEl.querySelectorAll('.line-solid');
        verticalLineEls = takeawayEl.querySelectorAll('.line-dashed');
        audioButtonEls = takeawayEl.querySelectorAll('.audio-button');
        audioTextEls = takeawayEl.querySelectorAll('.audio-text');
        nextButtonEl = takeawayEl.querySelector('.next-button');
        audioPlayers = takeawayEl.querySelectorAll('.audio');

        checkInactivity();
        setAudioEvents();
    });
</script>

